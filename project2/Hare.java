public class Hare {
   private int x;
	private int y;
   
   Hare() {
      ;
   }
   
   public int getHareX() {
      return x; 
   }
   
   public void setHareX(int hareLocX) {
      x = hareLocX;
   }
   
   public int getHareY() {
      return y;
   }
   
   public void setHareY(int hareLocY) {
      y = hareLocY;    
   }  
}
